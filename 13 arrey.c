#include <stdio.h>

int main()
{                   // Integer Array
    int a[5] = {10, 20, 30, 40, 50};

    printf("%d %d %d %d %d \n", a[0], a[1], a[2], a[3], a[4]);

                    // single character Array
    char b[8] = {'R', 'a', 'm', 'p', 'r', 'e', 'e', 't'};

    printf("%c%c%c%c%c%c%c%c", b[0], b[1], b[2], b[3], b[4], b[5], b[6], b[7]);

    return 0;
}
